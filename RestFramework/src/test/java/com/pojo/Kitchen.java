package com.pojo;

public class Kitchen {
	private String fridge;
	private String microwave;
	private String gassstove;
	public String getFridge() {
		return fridge;
	}
	public void setFridge(String fridge) {
		this.fridge = fridge;
	}
	public String getMicrowave() {
		return microwave;
	}
	public void setMicrowave(String microwave) {
		this.microwave = microwave;
	}
	public String getGassstove() {
		return gassstove;
	}
	public void setGassstove(String gassstove) {
		this.gassstove = gassstove;
	}
	
}
